package dto;

public class Recipe {
	private int product_id;
	private int milk;
	private int coffee;
	private int honeybread;
	private int muffin;
	private int cake;
	private int apple;
	private int orange;
	
	public int getProduct_id() {
		return product_id;
	}
	public void setProduct_id(int product_id) {
		this.product_id = product_id;
	}
	public int getMilk() {
		return milk;
	}
	public void setMilk(int milk) {
		this.milk = milk;
	}
	public int getCoffee() {
		return coffee;
	}
	public void setCoffee(int coffee) {
		this.coffee = coffee;
	}
	public int getHoneybread() {
		return honeybread;
	}
	public void setHoneybread(int honeybread) {
		this.honeybread = honeybread;
	}
	public int getMuffin() {
		return muffin;
	}
	public void setMuffin(int muffin) {
		this.muffin = muffin;
	}
	public int getCake() {
		return cake;
	}
	public void setCake(int cake) {
		this.cake = cake;
	}
	public int getApple() {
		return apple;
	}
	public void setApple(int apple) {
		this.apple = apple;
	}
	public int getOrange() {
		return orange;
	}
	public void setOrange(int orange) {
		this.orange = orange;
	}
	
	
	

}
